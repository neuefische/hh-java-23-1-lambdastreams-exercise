import java.util.Arrays;
import java.util.Collections;
import java.util.List;

public class CountingDuplicates {

    /*
    In dieser Übung wird von dir erwatet eine Methode zu implementieren,
        die einen String aufnimmt,
        zählt wie viele Buchstaben sich im String wiederholen,
        und die Anzahl der sich wiederholenden Buchstaben zurückgibt (oder 0, sollte es Keine geben).

    Hinweiß: Groß/Kleinschreibung soll ignoriert werden, Großbuchstaben und Kleinbuchstaben werden zusammen
        gezählt

    Beispiel:
        "abcdefg" -> keine sich wiederholenden Buchstaben -> gibt 0 wieder;
        "aabcdef" -> a wiederholt sich -> gibt 1 wieder;
        "AAABBBBAAA" -> a wiederholt sich, b wiederholt sich -> gibt 2 wieder;
     */

    public static int duplicateCount(String str) {
        // This is where your code goes!

        List<String> list = Arrays.asList(str.toLowerCase().split(""));
        return (int) list.stream()
                .filter(s -> Collections.frequency(list, s) > 1)
                .distinct()
                .count();
    }
}
